package Practices;

public class ForLoop {

	public static void main(String[] args) {
		 // for loop - execute limited amount of times
		
		for (int i = 10; i >= 0; i--) {
		//index declared | condition | increment/decrement
						
			System.out.println(i);
		}
		
		System.out.println("Yey!");

	}

}
